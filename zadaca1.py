mood = input("What mood are you in?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't be sad.")
elif mood == "excited":
    print("Relax.")
elif mood == "relaxed":
    print("Good.")
else:
    print("Ok.")
